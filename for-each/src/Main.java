import java.util.ArrayList;

public class Main {

	public static void main(String[] args) {
		
		
	
		// for-each = traversing tech to iterate throught the elements in an array/collection
		// less steps, more readible
		// less flexible
		
		
		
		
		//String[] animals = {"cat", "dog", "rat","bird"};
		
		ArrayList<String> animals = new ArrayList<String>();
		
		animals.add("cat");
		animals.add("dog");
		animals.add("rat");
		animals.add("bird");
		
		
		
		//colon represents the word in
		
		for(String i : animals) {
			System.out.println(i);
			
			
		}
	
	
	
	}

}
